void MainWindow::BinToFile(QTextStream* pStream, QString FileName)
{
    qint64 i;
    qint64 Size;
    qint64 TotalCounter;
    qint64 Counter;
    quint8 Byte;
    QString Temp;

    TotalCounter = 0;
    Counter = 0;

    // Stream data to file
    QFile File(FileName);
    
    if(File.open(QIODevice::ReadOnly))
    {
        Size = File.size();
        *pStream << "{\r\n    ";

        for(i = 0; i < Size; i++)
        {
            TotalCounter++;
            File.getChar((char*)&Byte);
            Temp = QString("%1").arg(Byte, 2, 16, QChar('0')).toUpper();
            *pStream << "0x" << Temp;

            if(TotalCounter != Size)
            {
               *pStream << ",";
                Counter++;

                if(Counter == 16)
                {
                    Counter = 0;
                    *pStream << "\r\n    ";
                }
            }
        }
        File.close();
   }

    *pStream << "\r\n};\r\n\r\n\r\n";
}

