void MainWindow::LoadImageConverter(int row, Resizer_e Resizer)
{
    QTableWidgetItem *item;
    QString Filename;
    QString PathAndFilename;

    ui->ButtonConvert->setEnabled(true);
    item = ui->TableFilesFound->item(row, 0);
    Filename        = item->text();
    PathAndFilename = m_CurrentDir.absoluteFilePath(Filename);
    m_FileSize      = QFileInfo(PathAndFilename).size();

    if(m_pImage != nullptr)
    {
        delete m_pImage;
        m_pImage = nullptr;
    }

    m_pImage = new QImage();
    m_pImage->load(PathAndFilename);

    //m_pImage = new QImage(m_pImage->convertToFormat(QImage::Format_ARGB32));
    // Information that do not change if setting change
    ui->LabelFilename->setText(item->text());
    ui->LabelFilename->setStyleSheet(QString("color: black;"));
    ui->LabelSize->setText(PrintSize(m_pImage->size()));
    ui->LabelSize->setStyleSheet(QString("color: black;"));

    if(Resizer == AUTO_FORMAT)
    {
        m_PixelFormatConverter = AutoSelectConversion(ui->comboBoxPixelFormat, m_pImage->format());
    }

    // Redraw Image
    QPoint  Point;
    QSize   Size;
    QImage* pResizedImage;

    ClearSceneConverter();

    if(m_pBackgroundImage != nullptr)
    {
        QGraphicsPixmapItem* pBg = m_SceneConverter.addPixmap(QPixmap::fromImage(*m_pBackgroundImage));
        pBg->setZValue(-10);   // Sous tout
        pBg->setPos(0, 0);
    }

    pResizedImage = new QImage();
    ScaleToRequirement(m_pImage, pResizedImage, &m_SkinConfig.DisplaySize, (Scale_e)ui->comboBoxResize->currentIndex());

    if(m_pProcessedImage != nullptr)
    {
        delete m_pProcessedImage;
        m_pProcessedImage = nullptr;
    }

    m_pProcessedImage = new QImage();
    *m_pProcessedImage = pResizedImage->convertToFormat(m_PixelFormatConverter);
    delete pResizedImage;

    // Set the size for the image info
    m_Scale.setWidth((m_pProcessedImage->width()   > m_SkinConfig.DisplaySize.width())  ? m_SkinConfig.DisplaySize.width()  : m_pProcessedImage->width());
    m_Scale.setHeight((m_pProcessedImage->height() > m_SkinConfig.DisplaySize.height()) ? m_SkinConfig.DisplaySize.height() : m_pProcessedImage->height());

    m_TotalCount  = m_pProcessedImage->sizeInBytes() / (m_pProcessedImage->width() * m_pProcessedImage->height());    // Adjust byte count to image size in viewport if it is the case
    m_TotalCount *= (m_Scale.width() * m_Scale.height());

    // Update Static Label
    ui->LabelStaticFileInfo->setStyleSheet(QString("color: black;"));
    ui->LabelStaticFilename->setStyleSheet(QString("color: black;"));
    ui->LabelStaticFileDataSize->setStyleSheet(QString("color: black;"));
    ui->LabelStaticDataSize->setStyleSheet(QString("color: black;"));
    ui->LabelStaticPixelFormat->setStyleSheet(QString("color: black;"));
    ui->LabelStaticSize->setStyleSheet(QString("color: black;"));
    ui->LabelStaticScaleSize->setStyleSheet(QString("color: black;"));
    ui->LabelStaticCheckerColor->setStyleSheet(QString("color: black;"));
    ui->LabelStaticConvertTo->setStyleSheet(QString("color: black;"));
    ui->LabelStaticResize->setStyleSheet(QString("color: black;"));
    ui->LabelStaticCompress->setStyleSheet(QString("color: black;"));

    ui->LabelPixelFormat->setText(GetFormat(m_pImage->format()));
    ui->LabelPixelFormat->setStyleSheet(QString("color: black;"));

    ui->LabelDataSize->setText(QString("%1 Bytes").arg(m_TotalCount));                                              // Print updated value in information
    ui->LabelDataSize->setStyleSheet(QString("color: black;"));

    ui->LabelFileDataSize->setText(QString("%1 Bytes").arg(m_FileSize));
    ui->LabelFileDataSize->setStyleSheet(QString("color: black;"));

    ui->LabelScaleSize->setText(PrintSize(m_Scale));                                                                // Display viewport size
    ui->LabelScaleSize->setStyleSheet(QString("color: black;"));

    // Set the size for the scene (minimum size is viewport)
    Size.setWidth ((m_pProcessedImage->width()  < m_SkinConfig.DisplaySize.width())  ? m_SkinConfig.DisplaySize.width()  : m_pProcessedImage->width());
    Size.setHeight((m_pProcessedImage->height() < m_SkinConfig.DisplaySize.height()) ? m_SkinConfig.DisplaySize.height() : m_pProcessedImage->height());

    if((Size.width()  > m_SkinConfig.DisplaySize.width()) ||                                                                   // Display special note if image is bigger than viewport
       (Size.height() > m_SkinConfig.DisplaySize.height()))
    {
        ui->LabelNoteConverter->setVisible(true);
    }
    else
    {
        ui->LabelNoteConverter->setVisible(false);
    }

    ui->horizontalScrollBarConverter->blockSignals(true);
    ui->verticalScrollBarConverter->blockSignals(true);
    ui->horizontalScrollBarConverter->setValue(0);
    ui->verticalScrollBarConverter->setValue(0);
    m_horizontalScrollRange = (m_pProcessedImage->size().width()  - m_SkinConfig.DisplaySize.width())  * 100;
    m_verticalScrollRange   = (m_pProcessedImage->size().height() - m_SkinConfig.DisplaySize.height()) * 100;
    ui->horizontalScrollBarConverter->setRange(0, m_horizontalScrollRange);
    ui->verticalScrollBarConverter->setRange  (0, m_verticalScrollRange);
    ui->horizontalScrollBarConverter->setSingleStep(m_horizontalScrollRange / 10);
    ui->verticalScrollBarConverter->setSingleStep  (m_verticalScrollRange / 10);
    ui->horizontalScrollBarConverter->blockSignals(false);
    ui->verticalScrollBarConverter->blockSignals(false);

    // Added image to the scene
    m_pPixmapItem = m_SceneConverter.addPixmap(QPixmap::fromImage(*m_pProcessedImage));                            // Add the image on top of the checker pattern
    Point = CenterPoint(m_pProcessedImage->size(), m_SkinConfig.DisplaySize);
    m_pPixmapItem->setPos(Point);
}

