int MainWindow::PrintArrayBody(QTextStream& Stream, const QVector<uint8_t>& Data, QString& BaseName, int IndexFormat, int& CompressionIndex)
{
    int ElementSize = (IndexFormat == FORMAT_RGB565) ? sizeof(uint16_t) : sizeof(uint32_t);
    int Count;
    bool IsItStruct = false;

    if(ElementSize == sizeof(uint16_t))
    {
        if(CompressionIndex == RLE_16)
        {
            Stream << "const StaticImageRLE_16_t " << BaseName;
            Count = Data.size() / (ElementSize + sizeof(uint8_t));
            IsItStruct = true;
        }
        else
        {
            Stream << "const uint16_t " << BaseName;
            Count = Data.size() / ElementSize;
        }
    }
    else // (ElementSize == sizeof(uint32_t))
    {
        if(CompressionIndex == RLE_32)
        {
            Stream << "const StaticImageRLE_32_t " << BaseName;
            Count = Data.size() / (ElementSize + sizeof(uint8_t));
            IsItStruct = true;
        }
        else
        {
            Stream << "const uint32_t " << BaseName;
            Count = Data.size() / ElementSize;
        }
    }

    Stream << "[" << Count << "] =\r\n{\r\n    ";

    int PerLine = 0;

    if(IsItStruct == false)
    {
        for(int i = 0; i < Count; i++)
        {
            QString Temp;

            if(ElementSize == 2)
            {
                uint16_t v = ((uint16_t)Data[2*i] << 8) | ((uint16_t)Data[2*i+1]);
                Temp = QString("%1").arg(v, 4, 16, QChar('0')).toUpper();
            }
            else
            {
                uint32_t v = ((uint32_t)Data[4*i]     << 24) |
                             ((uint32_t)Data[4*i + 1] << 16) |
                             ((uint32_t)Data[4*i + 2] << 8)  |
                             ((uint32_t)Data[4*i + 3]);
                Temp = QString("%1").arg(v, 8, 16, QChar('0')).toUpper();
            }

            Stream << "0x" << Temp;

            if(i + 1 < Count)
                Stream << ", ";

            PerLine++;

            if(PerLine == 16)
            {
                PerLine = 0;
                Stream << "\r\n    ";
            }
        }
    }
    else
    {
        int i = 0;

        while(i < Data.size())
        {
            // Read repeat (1 byte)
            uint8_t Repeat = Data[i];
            i += 1;

            // Read pixel (2 or 4 bytes)
            uint32_t pixel = 0;

            if(ElementSize == 2)   // RLE16
            {
                pixel = ((uint16_t)Data[i]   << 8) |
                        ((uint16_t)Data[i+1]);
                i += 2;
            }
            else                   // RLE32
            {
                pixel = ((uint32_t)Data[i]   << 24) |
                        ((uint32_t)Data[i+1] << 16) |
                        ((uint32_t)Data[i+2] << 8)  |
                        ((uint32_t)Data[i+3]);
                i += 4;
            }

            // Print struct
            Stream << "{ 0x"
                   << QString("%1").arg(Repeat, 2, 16, QChar('0')).toUpper()
                   << ", 0x"
                   << QString("%1").arg(pixel,
                                        (ElementSize == 2 ? 4 : 8),
                                        16,
                                        QChar('0')).toUpper()
                   << " }";

            PerLine++;

            // Add comma at end of line (except last element)
            if(i < Data.size())
                Stream << ", ";

            // Line break every 8 structs
            if(PerLine == 8 && i < Data.size())
            {
                Stream << "\r\n    ";
                PerLine = 0;
            }
        }
    }

    Stream << "\r\n};\r\n\r\n";

    return Count;
}

