// ************************************************************************************************

void MainWindow::on_TableFilesFound_cellActivated(int row, int column)
{
    QTableWidgetItem* item = ui->TableFilesFound->item(row, 0);
    QString FileName = m_CurrentDir.absoluteFilePath(item->text());
    QFileInfo FileInfo(FileName);
    QString BaseName = FileInfo.baseName();                     // Get the file name without extension
    QString Temp;
    QString Format;
    int IndexFormat;
    QVector<uint8_t> Data;
    int Count;
    int CompressionIndex;

    Q_UNUSED(column);

    // Replace '-' or ' ' by '_' with regex
    QRegExp Regex("[- ]+");
    int s = -1;
    while((s = Regex.indexIn(BaseName, s + 1)) >= 0)
    {
        BaseName[s] = '_';
    }

    // Replace all numeric from the beginning of the name
    Regex.setPattern("[^0-9]+[_A-Za-z0-9]+");
    if(Regex.indexIn(BaseName, 0) >= 0)
    {
        BaseName = Regex.cap(0);
    }

    // Stream data to file
    QFile File(BaseName + ".cpp");
    if(File.open(QIODevice::WriteOnly))
    {
        QTextStream Stream(&File);

        Stream << "//-------------------------------------------\r\n";
        Stream << "// This file was autogenerated by GUI Builder\r\n";
        Stream << "//-------------------------------------------\r\n\r\n";
        Stream << "#include \"./lib_digini.h\"\r\n\r\n";
        Stream << "#ifdef DIGINI_USE_GRAFX\r\n\r\n";
        Stream << "//-------------------------------------------\r\n\r\n";

        IndexFormat = ui->comboBoxPixelFormat->currentIndex();

        if((IndexFormat == FORMAT_RGB565) || (IndexFormat == FORMAT_ARGB8888))
        {
            this->Extract(&Data, IndexFormat, &CompressionIndex);
            Count = PrintArrayBody(Stream, Data, BaseName, IndexFormat, CompressionIndex);
        }

        Stream << "const StaticImageInfo_t SII_" << BaseName << " =\r\n";
        Stream << "{\r\n    {\r\n        (void*)";
        Stream << BaseName << ",\r\n";
        Temp = QString("%1").arg(m_Scale.width());
        Stream << "        {" << Temp.toUtf8() << ", ";
        Temp = QString("%1").arg(m_Scale.height());
        Stream << Temp.toUtf8() << "},\r\n        PIXEL_FORMAT_";

        switch(IndexFormat)
        {
        case FORMAT_RGB565:   { Format = QString("RGB565");   break;}
        case FORMAT_ARGB8888: { Format = QString("ARGB8888"); break;}
        }

        Stream << Format;
        Stream << "\r\n    },\r\n";
        Temp = QString("%1").arg(Count);
        Stream << "    " << Temp.toUtf8() << ",\r\n    COMPX_";

        switch(CompressionIndex)
        {
            case COMPRESSION_NONE:   Stream << "COMPRESSION_NONE";      break;
            case RLE_16:             Stream << "RLE_16";                break;
            case RLE_32:             Stream << "RLE_32";                break;
        }

        Stream << "\r\n};\r\n\r\n";
        Stream << "//-------------------------------------------------------------------------------------------------\r\n\r\n";
        Stream << "#endif // DIGINI_USE_GRAFX\r\n";


        File.close();
    }
}

